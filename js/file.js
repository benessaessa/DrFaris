function showMore()
{
    if ($('.threedots').css('display')=='none')
    {
        //already showing, we hide
            $('.threedots').show();
            $('.full_desc').hide();
    }
    else
    {
            //show more
            $('.threedots').hide();
            $('.full_desc').show();

    }


}
